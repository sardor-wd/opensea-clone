let colect = document.querySelector('.colect')

function collect(arr) {
    colect.innerHTML = ""

    for(let item of arr){
        let ant = document.createElement('div')             
        let imgMain = document.createElement('div')             
        let another = document.createElement('div')             
        let img = document.createElement('div')             
        let text = document.createElement('div') 
        
        let img1 = document.createElement('img')
        let img2 = document.createElement('img')

        let text1 = document.createElement('span');


        ant.classList.add('ant-carousel-element')
        imgMain.classList.add('img__main')
        another.classList.add('another')
        img.classList.add('img')
        text.classList.add('text')

        img1.src = item.mainImg
        img1.style.width = "414px"
        img1.style.height = "223px"
        img1.style.borderRadius = "10px 10px 0 0"

        img2.src = item.img
        img2.style.width = "70px"
        img2.style.height = "70px"
        img2.style.borderRadius = "10px"

        text1.innerHTML = item.name

        
        another.append(img, text)
        img.append(img2)
        text.append(text1)
        imgMain.append(img1)
        ant.append(imgMain, another)
        colect.append(ant)
    }
}
